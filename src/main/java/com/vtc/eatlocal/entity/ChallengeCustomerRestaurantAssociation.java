package com.vtc.eatlocal.entity;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name= "challenge_customer_restaurant_association")
public class ChallengeCustomerRestaurantAssociation {

    @Id
    private Integer customerId;

    private String challengeTitle;

    private String validatedRestaurantsList;

    public Integer getCustomerId() {
        return customerId;
    }

    public void setCustomerId(Integer customerId) {
        this.customerId = customerId;
    }

    public String getChallengeTitle() {
        return challengeTitle;
    }

    public void setChallengeTitle(String challengeTitle) {
        this.challengeTitle = challengeTitle;
    }

    public String getValidatedRestaurantsList() {
        return validatedRestaurantsList;
    }

    public void setValidatedRestaurantsList(String validatedRestaurantsList) {
        this.validatedRestaurantsList = validatedRestaurantsList;
    }
}
